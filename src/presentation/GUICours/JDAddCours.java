    private void fillComponents() {
        String jourSelect = (String) ComboBoxJour.getSelectedItem();
        String localSelect = (String) ComboBoxLocal.getSelectedItem();

        if (jourSelect != null && localSelect != null) {

            System.out.println("interieur fillcomp");
            JComboBox ComboBoxJour = new JComboBox();
            ComboBoxJour.addItem("test");
            /* charger tous les jours */
            for (int i = 0; i < jours.length; i++) {
                System.out.println(jours[i]);
                ComboBoxJour.addItem(String.valueOf(jours[i]));
            }

            /* charger touts les locaux */
            ArrayList<Local> loc = daoLoc.selectLocal();
            for (Local index : loc) {
                ComboBoxLocal.addItem(index.getNomLocal());
            }
        }
    }

