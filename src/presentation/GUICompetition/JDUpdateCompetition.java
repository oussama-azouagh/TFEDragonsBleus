   private void fillComponents(Competition comp)
    {
        tmpIdComp = comp.getIdCompetition();
        textFieldNomCompetition.setText(comp.getNom());
        dateChooserDateEvenement.setDate(comp.getDateEvenement());
        dateChooserLimiteInscription.setDate(comp.getDateLimiteInscription());
        formattedTextFieldPrixInscription.setText(String.valueOf(comp.getPrixInscription()));
        textFieldLieuEvenement.setText(comp.getLieu());
        formattedTextFieldDuree.setText(String.valueOf(comp.getDuree()));
        
        /* charger toutes les types de competition */
        for (int i=0;i<typeCompList.size();i++)
            comboBoxTypeCompetition.addItem((typeCompList.get(i)).getNomType());
        comboBoxTypeCompetition.setSelectedItem(comp.getidTypeCompetition().getIdTypeCompetition());
        
        //ComboBoxTypeCompetition.setSelectedIndex(comp.getidTypeCompetition().getIdTypeCompetition());   
   
    } 

