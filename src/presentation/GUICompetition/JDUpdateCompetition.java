    private void boutonConfirmerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonConfirmerActionPerformed
        // TODO add your handling code here:
                //confirmer
        
        Competition comp = new Competition();
        comp.setIdCompetition(tmpIdComp);
        comp.setNom(textFieldNomCompetition.getText());
        comp.setDateEvenement(dateChooserDateEvenement.getDate());
        comp.setDateLimiteInscription(dateChooserLimiteInscription.getDate());
        comp.setPrixInscription(Integer.parseInt(formattedTextFieldPrixInscription.getText()));
        comp.setLieu(textFieldLieuEvenement.getText());
        comp.setDuree(Integer.parseInt(formattedTextFieldDuree.getText()));
        
       //TypeCompetition tmp = new TypeCompetition(ComboBoxTypeCompetition.getSelectedIndex(),ComboBoxTypeCompetition.getSelectedItem().toString());
        
        //comp.setIdTypeCompetition(tmp); //
        TypeCompetition typeComp = typeCompList.stream().filter(type-> type.getNomType().equals((String)comboBoxTypeCompetition.getSelectedItem())).findFirst().get();
        comp.setidTypeCompetition(typeComp);
        boolean ok = daoComp.updateCompetition(comp);
        if (!ok)
            JOptionPane.showMessageDialog(null,"Modification impossible !","Avertissement",JOptionPane.ERROR_MESSAGE);
        
        this.dispose();
    }//GEN-LAST:event_boutonConfirmerActionPerformed

